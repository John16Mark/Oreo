%%VALORES DE X 
x=[1000000 2000000 3000000 4000000 5000000 6000000 7000000 8000000 9000000 10000000];
 
%%VALORES DE Y = F(x) en Milisegundos

%Búsqueda Lineal
y=[0.0026589036 0.0050716519 0.0074631095 0.0097561240 0.0120561361 0.0144011736 0.0165579319 0.0179617643 0.0200542569 0.0213015556];

%%OBTENER UN POLINOMIO QUE SE AJUSTE A LOS PUNTOS X Y Y
grado=1; 				%%GRADO DEL POLINOMIO DE AJUSTE
p=polyfit(x,y,grado);	%%FUNCION QUE REALIZA EL AJUSTE POLINOMIAL

%%SE CREA UN ESPACIO PARA REALIZAR UNA GRAFICA EN 
%%DONDE SE COMPARA LA CURVA AJUSTADA CONTRA LOS 
%%VALORES EXPERIMENTALES
xi=linspace(0,10000000,10000);      %%ESPACIO DE PUNTOS PARA Xi
z=polyval(p,xi);				%%EVALUACION DEL POLINOMIO P EN EL ESPACIO Xi

%%SE REALIZA LA FIGURA CORRESPONDIENTE
figure(1);
plot(x,y,'o',xi,z,'-',LineWidth=1),grid;
ylabel('F(x)');
xlabel('x');
title('Aproximación Busqueda lineal');