%%VALORES DE X
x=[1000000 2000000 3000000 4000000 5000000 6000000 7000000 8000000 9000000 10000000];

%%VALORES DE Y = F(x) en Milisegundos

    %Búsqueda Binaria
    %y=[0.0000008821 0.0000009656 0.0000010252 0.0000010848 0.0000010252 0.0000011683 0.0000013351 0.0000012159 0.0000014544 0.0000013947];
    
    %Búsqueda en ABB
    %y=[0.0000013828 0.0000017524 0.0000018954 0.0000018835 0.0000020504 0.0000022650 0.0000023246 0.0000021815 0.0000022888 0.0000022411];

    %Búsqueda Exponencial
    %y=[0.0000013947 0.0000016093 0.0000014305 0.0000014186 0.0000016093 0.0000016451 0.0000017047 0.0000025630 0.0000020146 0.0000019431];

    %Búsqueda de Fibonacci
    %y=[0.0000013351 0.0000013232 0.0000013113 0.0000015616 0.0000014186 0.0000012636 0.0000017524 0.0000015736 0.0000015616 0.0000020266];

%%OBTENER UN POLINOMIO QUE SE AJUSTE A LOS PUNTOS X Y Y
p = polyfit(log(x),y,1);
a=p(1);
c=p(2);
fprintf('f(x)=%f*ln(x)+%f\n',a,c);

%%SE CREA UN ESPACIO PARA REALIZAR UNA GRAFICA EN 
%%DONDE SE COMPARA LA CURVA AJUSTADA CONTRA LOS 
%%VALORES EXPERIMENTALES
xi=linspace(0,10000000,2000);      %%ESPACIO DE PUNTOS PARA Xi
z = polyval(p,log(xi));        %%EVALUACION DE LA CURVA LOGARITMICA P EN EL ESPACIO Xi

%%SE REALIZA LA FIGURA CORRESPONDIENTE
figure(1);
plot(x,y,'o',xi,z,'-',LineWidth=1),grid;
ylabel('F(x)');
xlabel('x');
title('Aproximación logarítmico y=a·ln(x)+c Busqueda Fibonacci');

